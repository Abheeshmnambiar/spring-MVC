package com.merchant.configuration;

public class MerchantHeader {

	private String applicationAccessKey;
	
	private String applicationSecretKey;

	public String getApplicationAccessKey() {
		return applicationAccessKey;
	}

	public void setApplicationAccessKey(String applicationAccessKey) {
		this.applicationAccessKey = applicationAccessKey;
	}

	public String getApplicationSecretKey() {
		return applicationSecretKey;
	}

	public void setApplicationSecretKey(String applicationSecretKey) {
		this.applicationSecretKey = applicationSecretKey;
	}
	
	
}
